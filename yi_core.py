# 渲染
# ---------------------------------------------------------------------------
LINE_GLYPH = {
    (0, "少阴"): "—   —",
    (0, "老阴"): "— × —",
    (1, "少阳"): "—————",
    (1, "老阳"): "— × —",
}

def hex_text(lines, label="卦"):
    out = [f"  [{label}]"]
    for i, ln in enumerate(lines, 1):
        v, lbl = ln[1], ln[2]
        out.append(f"    {i}爻  {LINE_GLYPH[(v, lbl)]}  {lbl} ({ln[0]})")
    return "\n".join(out)

def hex_info_text(h):
    num, name, up, lo, _, judgment, image, lines = h
    out = [f"  ── #{num} {name}({up}上 {lo}下) ──",
           f"  卦辞:{judgment}",
           f"  象传:{image}",
           "  爻辞:"]
    for ln in lines:
        out.append(f"    {ln}")
    return "\n".join(out)

