# ---------------------------------------------------------------------------
# 三钱法
# ---------------------------------------------------------------------------
def toss_three_coins():
    """掷三钱:字=2, 背=3. 返回 6/7/8/9."""
    return sum(random.choice([2, 3]) for _ in range(3))

def cast_six_lines():
    """六爻(自下而上). 每爻: (sum, value, label)
       6 老阴(yin,动)  7 少阳(yang)  8 少阴(yin)  9 老阳(yang,动)
    """
    M = {6: (0, "老阴"), 7: (1, "少阳"), 8: (0, "少阴"), 9: (1, "老阳")}
    return [(s, *M[s]) for s in (toss_three_coins() for _ in range(6))]

def line_to_yinyang(line):
    """爻 → 0/1. 动爻直接取最终值(老阳 9→yin=0, 老阴 6→yang=1)."""
    s, v, _ = line
    if s == 9: return 0
    if s == 6: return 1
    return v

def lines_to_binary(lines):
    return "".join(str(line_to_yinyang(l)) for l in lines)

def trigrams_of(binary):
    return tuple(int(c) for c in binary[:3]), tuple(int(c) for c in binary[3:])

def nuclear_hex(lines):
    """互卦:下 = 原卦 3,4,5 爻; 上 = 原卦 2,3,4 爻."""
    vals = [line_to_yinyang(l) for l in lines]
    return "".join(str(v) for v in (vals[2:5] + vals[1:4]))

def changed_binary(lines):
    return "".join(str(line_to_yinyang(l)) for l in lines)

