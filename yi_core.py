def search_hex(keyword):
    """按卦名、卦辞、爻辞模糊搜索."""
    kw = keyword.strip()
    if not kw: return []
    res = []
    for h in HEX:
        num, name, up, lo, _, j, img, lines = h
        blob = " ".join([name, up, lo, j, img, *lines])
        if kw in blob:
            res.append(h)
    return res
